// static keyword

// 1) static variable in function
// variable declared as static in a function are created and initialised once for the lifetime of the program


// 2) static variable in class
// -static varible in a class are created and initialised once,
// -they are shared by all the objects of the class

// 3)static object creation
// - static objects are created when the class is loaded into memory
// - they are destroyed when the class is unloaded from memory
// - exists till lifetime of the program
// - it will be present/exists until the last line of program is not executed