// Abstraction
// Abstraction is the process of hiding the implementation details of an object or a system from the outside world.
// Abstraction is a fundamental concept in object-oriented programming (OOP) that allows developers to focus on the essential features of an object or system while ignoring non-essential details.

// Abstraction helps to reduce complexity, improve modularity, and enhance maintainability.

// Abstraction is achieved through encapsulation, which involves bundling data and methods that operate on that data into a single unit, called a class or object. This encapsulation helps to hide the implementation details of an object from the outside world, exposing only the necessary information through a well-defined interface.

// Abstraction in OOP can be achieved through various techniques, including:

// 1.  **Encapsulation**: This involves bundling data and methods that operate on that data within a single unit, called a class or object. Encapsulation helps to hide the implementation details of
// an object from the outside world, making it easier to modify or extend the object without affecting
// other parts of the system.

// 2.  **Abstract classes and interfaces**: These are used to define a blueprint for objects that share a commonstructure or behavior. Abstract classes and interfaces provide a way to abstract away the implementation details of a class or interface, allowing developers to focus on the essential features.

// 3.  **Inheritance**: This is a mechanism that allows one class to inherit the properties and
// behaviors of another class. Inheritance enables developers to create a new class that is a modified
// version of an existing class, reducing code duplication and improving code reuse.

// 4.  **Polymorphism**: This is the ability of an object to take on multiple forms Polymorphism
// allows developers to write code that can work with objects of different classes, making the code more
// flexible and reusable.

// Abstraction is a powerful tool in OOP that helps to simplify complex systems, improve code
// reusability, and enhance maintainability.


