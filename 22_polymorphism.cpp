// Polymorphism
// -Polymorphism is the ability of an object to take on multiple forms. This is achieved by creating
// multiple classes that share a common base class. The base class contains the common attributes
// and methods, while the derived classes contain the attributes and methods that are specific to
// them.
// -constructor overloading is an example og polymorphism

// Types of polymorphism

// 1) Compile Time Polymorphism
// -This type of polymorphism is achieved through method overloading. In method overloading, multiple
// methods with the same name can be defined but with different parameters. The method that needs to be
// called is determined at compile time.
// -eg. constructor overloading, function overloading, operator overloading


// 2) Runtime Polymorphism
// -This type of polymorphism is achieved through method overriding. In method overriding, a method with
// the same name and parameters as a method in the base class is defined in the derived class. The
// method that needs to be called is determined at runtime.
// -eg. method overriding, function overriding, operator overriding, virtual function